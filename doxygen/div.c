/*! \fn int div(int a, int b)
 *  \brief La fonction réalise la division entière du paramètre a par le paramètre b.
 *  \param a Entier qui représente le numérateur.
 *  \param b Entier qui représente le dénominateur.
 *  \return Le quotient.
 */
int div(int a, int b) {
    return a/b;
}
